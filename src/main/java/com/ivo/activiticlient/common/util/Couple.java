package com.ivo.activiticlient.common.util;

/**
 * @author wangjian
 * @date 2018/10/12
 */
public class Couple {

    private String key;

    private String label;

    public Couple(String key, String label) {
        this.key = key;
        this.label = label;
    }

    public String getKey() {
        return key;
    }

    public String getLabel() {
        return label;
    }

}
